var thickness = function(txt, size) {
    var lin = 0;
    var rows = [[], [], [], [], [], [],[],[],[]];
    for (var i = 0; i < txt.length; i++) {
        var char = txt[i];
        if (alphabet[char]) {
            for (var j = 0; j < alphabet[char].length; j++) {
                var letters = alphabet[char][j];
                var row = rows[j+6*lin];
                for (var k = 0; k < letters.length; k++) {
                    row += (letters[k] === "1" ? "1" : "0");
                }
                row += "0";
                rows[j + 6*lin] = row;
            }
        } else if (char === "\n") {
            lin++;
            rows.push([]);
            rows.push([]);
            rows.push([]);
            rows.push([]);
            rows.push([]);
            rows.push([]);
        } else {
            continue;
        }
    }
    
    var longest = 0;
    for (var i = 0; i < rows.length; i++) {
        longest = max(rows[i].length, longest);
    }
    return longest * size;
};
